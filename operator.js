// Arithmetic Operators + - * / % (modulas) ** (exponentiation)
console.log('5 + 2 = ', 5 + 2);
console.log('5 - 2 = ', 5 - 2);
console.log('5 * 2 = ', 5 * 2);
console.log('5 / 2 = ', 5 / 2);

console.log('5 % 2 = ', 5 % 2); // remainder
console.log('5 ** 2 = ', 5 ** 3); // exponentiation 5^2


// Increment and Decrement Operators ++ --
let a = 5;
console.log('a = ', a);
console.log('++a pre-increment = ', ++a); // pre(age)-increment
console.log('a++ post-increment = ', a++); // post(pore)-increment
console.log('post-increment result = ', a); // post(pore)-increment

