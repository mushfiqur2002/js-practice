// string to number and number to string
// also convert in boolean to number and vice versa

let str = "123";
let num = 123;

console.log(str + 1); // "1231"
console.log(num + 1); // 124

console.log(str + num); // "123123" + operator has a dual purpose: 
                        // addition for numbers and concatenation for strings.

console.log(str - num); // 0

console.log(str * num); // 15129 * is a purely mathematical operation
                        // JavaScript tries to make sense of the operation in a mathematical context.

console.log(str / num); // 1
console.log(str * 2); // 246
console.log(str / 2); // 61.5

