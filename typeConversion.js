// string to number and number to string
// also convert in boolean to number and vice versaa
let str = "123";
let num = 123;

console.log(str + 1); // "1231"
console.log(num + 1); // 124

console.log(str + num); // "123123"
console.log(str - num); // 0
console.log(str * num); // 15129

console.log(str / num); // 1
